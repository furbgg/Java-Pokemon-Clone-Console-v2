    public List<Javamon> loadPokemon(String filePath) throws IOException {
        System.out.println("###LoadPokemon####");
        Path path = Paths.get(filePath);

        if (!Files.exists(path)) {
            System.out.println("Fehler" + filePath + "nicht gefunden");
            throw new FileNotFoundException(filePath + "nicht gefunden");
        }
        List<Javamon> javamonList;
        try {
            javamonList = Files.lines(path)
                    .skip(1)
                    .map(this::mapLineToJavamon)
                    .filter(a -> a != null)
                    .collect(Collectors.toList());
        } catch (IOException e) {
            System.out.println("Fehler!" + e.getMessage());
            return new ArrayList<>();
        }

        System.out.println(javamonList.size() + "Erfolgreich");
        return javamonList;
    }

    private Javamon mapLineToJavamon(String line) {
        String[] parts = line.split(";");

        if (parts.length < 11) {
            System.err.println("fehler");
            return null;
        }

        try {
            Javamon ja = new Javamon();
            ja.setId(Integer.parseInt(parts[0]));
            ja.setName(parts[1]);
            ja.setType(parts[2]);
            if (!parts[3].isEmpty()) {
                ja.setType2(parts[3].trim());
            } else {
                ja.setType2(null);
            }
            int hp = Integer.parseInt(parts[5]);
            ja.setHp(hp);
            ja.setMaxHp(hp);
            ja.setAttack(Integer.parseInt(parts[6]));
            ja.setDefense(Integer.parseInt(parts[7]));
            ja.setSpAtk(Integer.parseInt(parts[8]));
            ja.setSpDef(Integer.parseInt(parts[9]));
            ja.setSpeed(Integer.parseInt(parts[10]));
            ja.setAsciiArt(getAsciiArt(ja.getName()));
            return ja;
        } catch (Exception e) {
            System.err.println("Fehler " + e.getMessage());
            return null;
        }
    }
